import turtle
turtle.setup(650,350,200,200)
turtle.penup()
turtle.fd(-250)
turtle.pensize(20)
turtle.pendown()
turtle.pencolor("red")
turtle.seth(-40)
for i in range(8):
    turtle.circle(20,80)
    turtle.circle(-20,80)
turtle.circle(20,80/2)
turtle.fd(40)
turtle.circle(16,180)
turtle.fd(40*2/3)
turtle.done()